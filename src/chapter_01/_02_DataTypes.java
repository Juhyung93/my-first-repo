package chapter_01;

public class _02_DataTypes {
    public static void main(String[] args) {
        int A=10;
        System.out.println(A);
        System.out.println("안녕하세용");
        for (int i = 0; i < 10; i++) {
            A+=i;

            if (A ==13 ) {
                break;
            }
            System.out.println(A);
        }
        System.out.println(true);

    }
}
